#include "Date.h"

int Date::getMonth() {
	return this->month;
}

void Date::setMonth(int month) {
	this->month = month;
}

int Date::getDay() {
	return this->day;
}

void Date::setDay(int day) {
	this->day = day;
}

int Date::getYear() {
	return this->year;
}

void Date::setYear(int year) {
	this->year = year;
}
